public class Dought {
    private String whiteDought;
    private String wholegrainDought;

    public Dought(String whiteDought, String wholegrainDought) {
        this.whiteDought = whiteDought;
        this.wholegrainDought = wholegrainDought;
    }

    public String getWhiteDought() {
        return whiteDought;
    }

    public void setWhiteDought(String whiteDought) {
        this.whiteDought = whiteDought;
    }

    public String getWholegrainDought() {
        return wholegrainDought;
    }

    public void setWholegrainDought(String wholegrainDought) {
        this.wholegrainDought = wholegrainDought;
    }
}
